def check_guess(guess, secret):
    """
    Compare guess to secret and return (outcome, message).

    outcome examples: "Win", "Too High", "Too Low"
    """
    # FIX: Claude (agent mode) diagnosed the high/low bug — secret arrives as a
    # str on even attempts — and applied this int-coercion fix after I confirmed it.
    # Coerce both to int so comparison is always numeric. Previously, when
    # secret arrived as a str, `guess > secret` compared lexicographically
    # (e.g. "9" > "80" is True), which told the player the wrong direction.
    guess = int(guess)
    secret = int(secret)

    if guess == secret:
        return "Win", "🎉 Correct!"

    if guess > secret:
        return "Too High", "📉 Go LOWER!"

    return "Too Low", "📈 Go HIGHER!"


